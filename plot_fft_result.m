%% ===============================
% FFT INPUT VISUALIZATION
% ===============================

clear; clc; close all;

N = 128;

input_data = load('fft_input_complex.txt');
re_in = input_data(:,1);
im_in = input_data(:,2);

x = re_in + 1j*im_in;
n = 0:N-1;

figure('Name','Input Signal - Time Domain','Color','w');

% -------- REAL PART --------
subplot(3,1,1);
stem(n, re_in, ...
    'Color',[0 0.447 0.741], ...
    'LineWidth',1.5, ...
    'Marker','o','MarkerFaceColor',[0 0.447 0.741]);
grid on;
title('Input Signal – Real Part', ...
      'FontSize',16,'FontWeight','bold','Color','k');
xlabel('Time index n (sample domain)', ...
       'FontSize',14,'FontWeight','bold','Color','k');
ylabel('Re\{x[n]\} ', ...
       'FontSize',14,'FontWeight','bold','Color','k');
set(gca,'FontSize',13,'LineWidth',1.5, ...
        'XColor','k','YColor','k');

% -------- IMAG PART --------
subplot(3,1,2);
stem(n, im_in, ...
    'Color',[0.466 0.674 0.188], ...
    'LineWidth',1.5, ...
    'Marker','o','MarkerFaceColor',[0.466 0.674 0.188]);
grid on;
title('Input Signal – Imaginary Part', ...
      'FontSize',16,'FontWeight','bold','Color','k');
xlabel('Time index n (sample domain)', ...
       'FontSize',14,'FontWeight','bold','Color','k');
ylabel('Im\{x[n]\} ', ...
       'FontSize',14,'FontWeight','bold','Color','k');
set(gca,'FontSize',13,'LineWidth',1.5, ...
        'XColor','k','YColor','k');

% -------- MAGNITUDE --------
subplot(3,1,3);
stem(n, abs(x), ...
    'Color',[0.85 0.325 0.098], ...
    'LineWidth',1.5, ...
    'Marker','o','MarkerFaceColor',[0.85 0.325 0.098]);
grid on;
title('Input Signal – Magnitude', ...
      'FontSize',16,'FontWeight','bold','Color','k');
xlabel('Time index n (sample domain)', ...
       'FontSize',14,'FontWeight','bold','Color','k');
ylabel('|x[n]| ', ...
       'FontSize',14,'FontWeight','bold','Color','k');
set(gca,'FontSize',13,'LineWidth',1.5, ...
        'XColor','k','YColor','k');

sgtitle('Complex Fixed-Point FFT Input (Time Domain)', ...
        'FontSize',18,'FontWeight','bold','Color','k');

%% ===============================
% FFT OUTPUT VISUALIZATION
% ===============================

output_data = load('fft_output_complex.txt');
re_out = output_data(:,1);
im_out = output_data(:,2);

X = re_out + 1j*im_out;
k = 0:N-1;
mag = abs(X);

figure('Name','FFT Output Result','Color','w');

stem(k, mag, ...
    'Color',[0 0.447 0.741], ...
    'LineWidth',1.6, ...
    'Marker','o','MarkerFaceColor',[0 0.447 0.741]);
grid on;
title('FFT Output Result – Magnitude Spectrum', ...
      'FontSize',18,'FontWeight','bold','Color','k');
xlabel('Frequency index k ', ...
       'FontSize',14,'FontWeight','bold','Color','k');
ylabel('|X[k]| (Amplitude)', ...
       'FontSize',14,'FontWeight','bold','Color','k');
set(gca,'FontSize',13,'LineWidth',1.6, ...
        'XColor','k','YColor','k');

% -------- Highlight dominant frequency components --------
threshold = 0.6 * max(mag);
hold on;
peak_idx = find(mag > threshold);

stem(peak_idx-1, mag(peak_idx), ...
    'r','LineWidth',2, ...
    'Marker','o','MarkerFaceColor','r');

for p = peak_idx'
    text(p-1, mag(p), ...
        sprintf('k = %d', p-1), ...
        'FontSize',10, ...
        'FontWeight','bold', ...
        'Color','w', ...
        'VerticalAlignment','bottom');
end

legend({'|X[k]|','Dominant frequency components'}, ...
       'FontSize',12, ...
       'FontWeight','bold', ...
       'TextColor','w', ...
       'Location','best');
